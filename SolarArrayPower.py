def get_array_power(p_d, p_e, t_d, t_e, x_d, x_e):
    """
    Computes the power that must be generated by solar cells based on:
    p_d - power to be delivered to spacecraft during nominal conditions
    p_e - power to be delivered to spacecraft during eclipse conditions
    t_d - time the spacecraft is in nominal conditions
    t_e - time the spacecraft is in eclipse condition
    x_d - total efficiency from array to space craft in nominal conditions
    x_e - total efficiency from array to space craft (through batter!) in eclipse condition
    """

    return (((p_d * t_d) / x_d) + (p_e * t_e) / x_e) / t_d
